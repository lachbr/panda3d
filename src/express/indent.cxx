#include "indent.h"

////////////////////////////////////////////////////////////////////
//     Function: indent 
//  Description:
////////////////////////////////////////////////////////////////////
ostream &
indent(ostream &out, int indent_level) {
  for (int i = 0; i < indent_level; i++) {
    out << ' ';
  }
  return out;
}
