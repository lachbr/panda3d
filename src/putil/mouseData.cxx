MouseData::
MouseData() {
  _in_window = false;
  _xpos = 0;
  _ypos = 0;
}

////////////////////////////////////////////////////////////////////
//     Function: MouseData::get_x
//       Access: Public
//  Description:
////////////////////////////////////////////////////////////////////
int MouseData::
get_x() const {
  return _xpos;
}

////////////////////////////////////////////////////////////////////
//     Function: MouseData::get_y
//       Access: Public
//  Description:
////////////////////////////////////////////////////////////////////
int MouseData::
get_y() const {
  return _ypos;
}

////////////////////////////////////////////////////////////////////
//     Function: MouseData::get_in_window
//       Access: Public
//  Description:
////////////////////////////////////////////////////////////////////
bool MouseData::
get_in_window() const {
  return _in_window;
}
