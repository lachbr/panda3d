////////////////////////////////////////////////////////////////////
//     Function : bounded_rand
//       Access : Protected
//  Description : Returns a float in [0, 1]
////////////////////////////////////////////////////////////////////
float LinearRandomForce::
bounded_rand(void) {
  int val = rand() & 0x7fffffff;
  float f_val = (float) val / (float) 0x7fffffff;

  return f_val;
}
