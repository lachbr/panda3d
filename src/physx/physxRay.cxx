#ifdef HAVE_PHYSX

#include "physxRay.h"

#include "luse.h"


////////////////////////////////////////////////////////////////////
//     Function : PhysxRay
//       Access : Published
//  Description :
////////////////////////////////////////////////////////////////////
PhysxRay::
PhysxRay() {
  nRay = new NxRay();
}

////////////////////////////////////////////////////////////////////
//     Function : PhysxRay
//       Access : Published
//  Description :
////////////////////////////////////////////////////////////////////
PhysxRay::
PhysxRay(const LVecBase3f & _orig, const LVecBase3f & _dir) {
  nRay = new NxRay(PhysxManager::lVecBase3_to_nxVec3(_orig), PhysxManager::lVecBase3_to_nxVec3(_dir));
}

////////////////////////////////////////////////////////////////////
//     Function : ~PhysxRay
//       Access : Published
//  Description :
////////////////////////////////////////////////////////////////////
PhysxRay::
~PhysxRay() {
  delete nRay;
}

////////////////////////////////////////////////////////////////////
//     Function : get_dir
//       Access : Published
//  Description :
////////////////////////////////////////////////////////////////////
LVecBase3f PhysxRay::
get_dir() const {
  nassertr(nRay != NULL, *((LVecBase3f *)NULL));

  return PhysxManager::nxVec3_to_lVecBase3(nRay->dir);
}

////////////////////////////////////////////////////////////////////
//     Function : get_orig
//       Access : Published
//  Description :
////////////////////////////////////////////////////////////////////
LVecBase3f PhysxRay::
get_orig() const {
  nassertr(nRay != NULL, *((LVecBase3f *)NULL));

  return PhysxManager::nxVec3_to_lVecBase3(nRay->orig);
}

////////////////////////////////////////////////////////////////////
//     Function : set_dir
//       Access : Published
//  Description :
////////////////////////////////////////////////////////////////////
void PhysxRay::
set_dir(LVecBase3f value) {
  nassertv(nRay != NULL);

  nRay->dir = PhysxManager::lVecBase3_to_nxVec3(value);
}

////////////////////////////////////////////////////////////////////
//     Function : set_orig
//       Access : Published
//  Description :
////////////////////////////////////////////////////////////////////
void PhysxRay::
set_orig(LVecBase3f value) {
  nassertv(nRay != NULL);

  nRay->orig = PhysxManager::lVecBase3_to_nxVec3(value);
}

#endif // HAVE_PHYSX

