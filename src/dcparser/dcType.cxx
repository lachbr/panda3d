////////////////////////////////////////////////////////////////////
//     Function: DCType::get_name
//       Access: Published, Virtual
//  Description: Returns a string that uniquely describes this type.
////////////////////////////////////////////////////////////////////
string DCType::
get_name() const {
  // The default get_name() implementation simply returns the result
  // of output().
  ostringstream result;
  output(result, "", true);
  return result.str();
}

